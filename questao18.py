#Leia um valor de volume em metros cúbicos m3 e apresente-o convertido em litros. 
#A formula de conversão é: L = 1000∗ M, sendo L o volume em litros e M o volume em metros cúbicos
 
metro_3 = float(input("Informe um valor para conversão de metro cúbico para litros: "))
 
litros = 1000*metro_3
print(f"A conversão de metro_3 para litros é: {litros}litros")
